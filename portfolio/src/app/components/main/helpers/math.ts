export class Math
{
	public static Add(a: number, b: number): number
	{
		return a + b;
	}

	public static Sub(a: number, b: number): number
	{
		return a - b;
	}

	public static Mul(a: number, b: number): number
	{
		return a * b;
	}

	public static Div(a: number, b: number): number
	{
		return a / b;
	}
}
